// Handle server errors
server.on('error', (err) => {
  if (err.code === 'EADDRINUSE') {
    console.error(`âŒ Port ${PORT} is already in use. Please stop the other server or use a different port.`);
  } else {
    console.error('âŒ Server error:', err);
  }
});

